
a = 10
b = 3


suma = a + b
impartire_reala = a / b      
impartire_intreaga = a // b
restul = a % b
putere = a ** b

print(f"Suma: {suma}, Tip: {type(suma)}")
print(f"Împărțire reală: {impartire_reala}, Tip: {type(impartire_reala)}")
print(f"Împărțire întreagă: {impartire_intreaga}")
print(f"Restul împărțirii (10 la 3): {restul}")
print(f"10 la puterea 3: {putere}")
